
        import { API_VERSION, ORIGIN } from "../../constants";
import { director } from "../../models/director";
import { HttpResult } from "../../utils";

export const get_directors = (): Promise<HttpResult<director[]>> =>
    fetch(ORIGIN + "/api/" + API_VERSION + "/director/Get.php")
        .then(response => {
            if (response.ok) {
                return response.json()
            }
            return Promise.reject(response.statusText)
        })
        .then(data => (<HttpResult<director[]>>{ kind: 'result', status: data._status, value: data._value }))
        .catch(reason => {
            console.log(reason)
            return ({ kind: 'failed', status: 500 })
        })

