
        import { API_VERSION, ORIGIN } from "../../constants";
import { reservations } from "../../models/reservations";
import { HttpResult } from "../../utils";

export const get_reservationss = (): Promise<HttpResult<reservations[]>> =>
    fetch(ORIGIN + "/api/" + API_VERSION + "/reservations/Get.php")
        .then(response => {
            if (response.ok) {
                return response.json()
            }
            return Promise.reject(response.statusText)
        })
        .then(data => (<HttpResult<reservations[]>>{ kind: 'result', status: data._status, value: data._value }))
        .catch(reason => {
            console.log(reason)
            return ({ kind: 'failed', status: 500 })
        })

