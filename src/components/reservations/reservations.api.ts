export const createreservations = (body: Omit<reservations, 'Id'>): Promise<number> =>
    fetch(ORIGIN + "/api/" + API_VERSION + "/reservations/Create.php", {
        body: JSON.stringify(body),
        method: 'post',
        mode: 'cors'
    })
        .then(response => {
            if (response.ok) {
                return response.json()
            }
            return Promise.reject(response.statusText)
        })
        .then(data => data._status)
        .catch(reason => {
            console.log(reason)
            return -1
        })

